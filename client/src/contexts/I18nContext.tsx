export type TranslationKeys = typeof translations.zh;

// ─── Context ──────────────────────────────────────────────────────────────────
interface I18nContextType {
  lang: Language;
  setLang: (lang: Language) => void;
  t: TranslationKeys;
}

const I18nContext = createContext<I18nContextType | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Language>(() => {
    const stored = localStorage.getItem("masterai_lang");
    return (stored as Language) || "zh";
  });

  const setLang = useCallback((newLang: Language) => {
    setLangState(newLang);
    localStorage.setItem("masterai_lang", newLang);
  }, []);

  // Apply font changes to document root when language changes
  useEffect(() => {
    const fonts = LANG_FONTS[lang];
    const root = document.documentElement;
    root.style.setProperty("--font-body", fonts.body);
    root.style.setProperty("--font-display", fonts.display);
    root.style.setProperty("--font-mono", fonts.mono);
    root.setAttribute("lang", lang === "zh" ? "zh-CN" : lang === "ja" ? "ja" : "en");
  }, [lang]);

  const t = translations[lang] as unknown as TranslationKeys;

  return (
    <I18nContext.Provider value={{ lang, setLang, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used inside I18nProvider");
  return ctx;
}
